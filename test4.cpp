#include <iostream>
using namespace std;

int main()
{
	int a = 192;
	int b = 64;
	cout << (~a) << endl;
	//0000 0000 0000 0000 0000 0000 1100 0000 
	//->
	//1111 1111 1111 1111 1111 1111 0011 1111 
	cout << (b << 2) << endl;
	//1000000(64) -> 100000000(256)
	cout << (b >> 2) << endl;
	//1000000(64) -> 0010000(16)
	//********************************************//
	cout << (a & b) << endl;
	//1100 0000 AND 0100 0000 = 0100 0000
	cout << (a | b) << endl;
	//1100 0000 OR 0100 0000 = 1100 0000
	cout << (a ^ b) << endl;
	//1100 0000 XOR 0100 0000 = 1000 0000
	//********************************************//
}